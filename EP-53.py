# Day-53: Operator Overloading

"""
Operator overloading is a mechanism that 
allows us to give new meaning to existing operators

"""

# Python program to show use of
print(3+5)
# + operator for different purposes.
# concatenate two strings
print("project"+"maker")

# Product two numbers
print(3*4)

# Repeat the String
print("project "*5)





